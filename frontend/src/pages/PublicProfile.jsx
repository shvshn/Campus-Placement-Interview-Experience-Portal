import { useState, useEffect } from 'react';
import { useParams, Link, useNavigate } from 'react-router-dom';
import { userAPI } from '../services/api.js';
import './PublicProfile.css';

function PublicProfile() {
    const { username } = useParams();
    const navigate = useNavigate();
    const [profile, setProfile] = useState(null);
    const [experiences, setExperiences] = useState([]);
    const [loading, setLoading] = useState(true);
    const [error, setError] = useState(null);

    useEffect(() => {
        loadProfile();
    }, [username]);

    const loadProfile = async () => {
        try {
            setLoading(true);
            setError(null);
            const data = await userAPI.getPublicProfile(username);
            if (data.success) {
                setProfile(data.user);
                setExperiences(data.experiences || []);
            }
        } catch (err) {
            console.error('Failed to load profile:', err);
            setError('User not found');
        } finally {
            setLoading(false);
        }
    };

    const getInitials = (name) => {
        if (!name) return '?';
        const parts = name.trim().split(' ');
        if (parts.length === 1) return parts[0].substring(0, 2).toUpperCase();
        return (parts[0][0] + parts[parts.length - 1][0]).toUpperCase();
    };

    const formatDate = (dateString) => {
        if (!dateString) return 'N/A';
        return new Date(dateString).toLocaleDateString('en-IN', {
            day: 'numeric',
            month: 'short',
            year: 'numeric',
        });
    };

    const getStatusClass = (status) => {
        switch (status) {
            case 'Selected': return 'status-selected';
            case 'Not Selected': return 'status-rejected';
            default: return 'status-pending';
        }
    };

    if (loading) {
        return (
            <div className="public-profile">
                <div className="container">
                    <Link to="#" onClick={(e) => { e.preventDefault(); navigate(-1); }} className="back-link">
                        Back
                    </Link>
                    <div className="profile-loading">
                        <div className="loading-spinner" />
                        <p>Loading profile...</p>
                    </div>
                </div>
            </div>
        );
    }

    if (error || !profile) {
        return (
            <div className="public-profile">
                <div className="container">
                    <Link to="#" onClick={(e) => { e.preventDefault(); navigate(-1); }} className="back-link">
                        Back
                    </Link>
                    <div className="profile-error">
                        <h2>User not found</h2>
                        <p>The user @{username} doesn't exist.</p>
                        <Link to="/" className="back-link">← Back to Home</Link>
                    </div>
                </div>
            </div>
        );
    }

    return (
        <div className="public-profile">
            <div className="container">
                <Link to="#" onClick={(e) => { e.preventDefault(); navigate(-1); }} className="back-link">
                    Back
                </Link>
                
                {/* Profile Header */}
                <div className="public-profile-card">
                    <div className="profile-avatar-section">
                        <div className="profile-avatar-large">
                            {getInitials(profile.name)}
                        </div>
                        <div className="profile-basic-info">
                            <h1>{profile.name}</h1>
                            <span className="profile-username">@{profile.username}</span>
                            <div className="profile-badges">
                                <span className="role-badge">{profile.role}</span>
                                {profile.isAlumni && <span className="alumni-badge">Alumni</span>}
                            </div>
                        </div>
                    </div>

                    <div className="profile-details">
                        {profile.branch && (
                            <div className="profile-detail">
                                <svg viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2">
                                    <path d="M2 3h6a4 4 0 0 1 4 4v14a3 3 0 0 0-3-3H2z" />
                                    <path d="M22 3h-6a4 4 0 0 0-4 4v14a3 3 0 0 1 3-3h7z" />
                                </svg>
                                <span>{profile.branch}</span>
                            </div>
                        )}
                        {profile.graduationYear && (
                            <div className="profile-detail">
                                <svg viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2">
                                    <rect x="3" y="4" width="18" height="18" rx="2" ry="2" />
                                    <line x1="16" y1="2" x2="16" y2="6" />
                                    <line x1="8" y1="2" x2="8" y2="6" />
                                    <line x1="3" y1="10" x2="21" y2="10" />
                                </svg>
                                <span>Class of {profile.graduationYear}</span>
                            </div>
                        )}
                        {profile.currentCompany && (
                            <div className="profile-detail">
                                <svg viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2">
                                    <rect x="2" y="7" width="20" height="14" rx="2" ry="2" />
                                    <path d="M16 21V5a2 2 0 0 0-2-2h-4a2 2 0 0 0-2 2v16" />
                                </svg>
                                <span>{profile.currentCompany}</span>
                            </div>
                        )}
                    </div>

                    {profile.profile?.bio && (
                        <div className="profile-bio">
                            <p>{profile.profile.bio}</p>
                        </div>
                    )}

                    {/* Social Links */}
                    {(profile.profile?.linkedin || profile.profile?.github || profile.profile?.twitter) && (
                        <div className="profile-social">
                            {profile.profile.linkedin && (
                                <a href={profile.profile.linkedin} target="_blank" rel="noopener noreferrer" className="social-link linkedin">
                                    <svg viewBox="0 0 24 24" fill="currentColor">
                                        <path d="M20.447 20.452h-3.554v-5.569c0-1.328-.027-3.037-1.852-3.037-1.853 0-2.136 1.445-2.136 2.939v5.667H9.351V9h3.414v1.561h.046c.477-.9 1.637-1.85 3.37-1.85 3.601 0 4.267 2.37 4.267 5.455v6.286zM5.337 7.433c-1.144 0-2.063-.926-2.063-2.065 0-1.138.92-2.063 2.063-2.063 1.14 0 2.064.925 2.064 2.063 0 1.139-.925 2.065-2.064 2.065zm1.782 13.019H3.555V9h3.564v11.452z" />
                                    </svg>
                                </a>
                            )}
                            {profile.profile.github && (
                                <a href={profile.profile.github} target="_blank" rel="noopener noreferrer" className="social-link github">
                                    <svg viewBox="0 0 24 24" fill="currentColor">
                                        <path d="M12 .297c-6.63 0-12 5.373-12 12 0 5.303 3.438 9.8 8.205 11.385.6.113.82-.258.82-.577 0-.285-.01-1.04-.015-2.04-3.338.724-4.042-1.61-4.042-1.61C4.422 18.07 3.633 17.7 3.633 17.7c-1.087-.744.084-.729.084-.729 1.205.084 1.838 1.236 1.838 1.236 1.07 1.835 2.809 1.305 3.495.998.108-.776.417-1.305.76-1.605-2.665-.3-5.466-1.332-5.466-5.93 0-1.31.465-2.38 1.235-3.22-.135-.303-.54-1.523.105-3.176 0 0 1.005-.322 3.3 1.23.96-.267 1.98-.399 3-.405 1.02.006 2.04.138 3 .405 2.28-1.552 3.285-1.23 3.285-1.23.645 1.653.24 2.873.12 3.176.765.84 1.23 1.91 1.23 3.22 0 4.61-2.805 5.625-5.475 5.92.42.36.81 1.096.81 2.22 0 1.606-.015 2.896-.015 3.286 0 .315.21.69.825.57C20.565 22.092 24 17.592 24 12.297c0-6.627-5.373-12-12-12" />
                                    </svg>
                                </a>
                            )}
                            {profile.profile.twitter && (
                                <a href={profile.profile.twitter} target="_blank" rel="noopener noreferrer" className="social-link twitter">
                                    <svg viewBox="0 0 24 24" fill="currentColor">
                                        <path d="M18.244 2.25h3.308l-7.227 8.26 8.502 11.24H16.17l-5.214-6.817L4.99 21.75H1.68l7.73-8.835L1.254 2.25H8.08l4.713 6.231zm-1.161 17.52h1.833L7.084 4.126H5.117z" />
                                    </svg>
                                </a>
                            )}
                        </div>
                    )}
                </div>

                {/* Experiences Section */}
                <div className="public-experiences-section">
                    <div className="section-header">
                        <h2>
                            <svg viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2">
                                <path d="M14 2H6a2 2 0 0 0-2 2v16a2 2 0 0 0 2 2h12a2 2 0 0 0 2-2V8z" />
                                <polyline points="14 2 14 8 20 8" />
                            </svg>
                            Interview Experiences
                        </h2>
                        <span className="experience-count">{experiences.length} total</span>
                    </div>

                    {experiences.length === 0 ? (
                        <div className="experiences-empty">
                            <p>No experiences shared yet</p>
                        </div>
                    ) : (
                        <div className="experiences-list">
                            {experiences.map((exp) => (
                                <Link key={exp._id} to={`/experiences/${exp._id}`} className="experience-item">
                                    <div className="experience-main">
                                        <div className="experience-header">
                                            <h3>{exp.company}</h3>
                                            <span className="experience-role">{exp.role}</span>
                                        </div>
                                        <div className="experience-meta">
                                            <span>{exp.branch}</span>
                                            <span className="separator">•</span>
                                            <span>{exp.year}</span>
                                            {exp.package && (
                                                <>
                                                    <span className="separator">•</span>
                                                    <span className="package">{exp.package}</span>
                                                </>
                                            )}
                                        </div>
                                    </div>
                                    <div className="experience-details">
                                        <span className={`status ${getStatusClass(exp.offerStatus)}`}>
                                            {exp.offerStatus}
                                        </span>
                                        <span className="date">{formatDate(exp.createdAt)}</span>
                                    </div>
                                </Link>
                            ))}
                        </div>
                    )}
                </div>
            </div>
        </div>
    );
}

export default PublicProfile;
